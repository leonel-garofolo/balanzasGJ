package com.balanzasgj.app.informes.model;

public class Remito {
	
	private String denominacion;
	private String domicilio;
	private String localidad;
	private String provincia;
	private String CUIT;
	private String conductor;
	private String patente;
	private String acoplado;
	private String transporte;	
	private String pesoEntrada;
	private String pesoSalida;
	
	public String getDenominacion() {
		return denominacion;
	}
	public void setDenominacion(String denominacion) {
		this.denominacion = denominacion;
	}
	public String getDomicilio() {
		return domicilio;
	}
	public void setDomicilio(String domicilio) {
		this.domicilio = domicilio;
	}
	public String getLocalidad() {
		return localidad;
	}
	public void setLocalidad(String localidad) {
		this.localidad = localidad;
	}
	public String getProvincia() {
		return provincia;
	}
	public void setProvincia(String provincia) {
		this.provincia = provincia;
	}
	public String getCUIT() {
		return CUIT;
	}
	public void setCUIT(String cUIT) {
		CUIT = cUIT;
	}
	public String getConductor() {
		return conductor;
	}
	public void setConductor(String conductor) {
		this.conductor = conductor;
	}
	public String getPatente() {
		return patente;
	}
	public void setPatente(String patente) {
		this.patente = patente;
	}
	public String getAcoplado() {
		return acoplado;
	}
	public void setAcoplado(String acoplado) {
		this.acoplado = acoplado;
	}
	public String getTransporte() {
		return transporte;
	}
	public void setTransporte(String transporte) {
		this.transporte = transporte;
	}
	public String getPesoEntrada() {
		return pesoEntrada;
	}
	public void setPesoEntrada(String pesoEntrada) {
		this.pesoEntrada = pesoEntrada;
	}
	public String getPesoSalida() {
		return pesoSalida;
	}
	public void setPesoSalida(String pesoSalida) {
		this.pesoSalida = pesoSalida;
	}		
}
